class Calculator {
    // Addition of two numbers
    public int add(int[] numbers) {
        return numbers[0] + numbers[1];
    }
        // Subtraction of two numbers
    public int subtract(int[] numbers) {
        return numbers[0] - numbers[1];
    }
        // Multiplication of two numbers
    public int multiply(int[] numbers) {
        return numbers[0] * numbers[1];
    }



}

